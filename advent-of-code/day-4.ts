const getOne = (oneIn: string) => {
  const rows = oneIn.split("\n");
  let sum = 0;
  // console.log(rows);
  for (let rowInd = 0; rowInd < rows.length; rowInd++) {
    const row = rows[rowInd];
    // go across
    for (let j = 0; j < row.length - 3; j++) {
      const checkHor = row.substring(j, j + 4);
      if (checkHor === "XMAS" || checkHor === "SAMX") {
        sum++;
      }
    }

    // go down
    for (let i = 0; i < row.length; i++) {
      if (rowInd < rows.length - 3) {
        let down = "";
        while (down.length < 4) {
          down += rows[rowInd + down.length][i];
        }
        if (down === "XMAS" || down === "SAMX") {
          sum++;
        }

        if (i <= row.length - 3) {
          let diagRight = "";
          let shiftRight = 0;
          while (diagRight.length < 4) {
            diagRight += rows[rowInd + diagRight.length][i + shiftRight];
            shiftRight++;
          }

          if (diagRight === "XMAS" || diagRight === "SAMX") {
            sum++;
          }
        }

        if (i >= 3) {
          let diagLeft = "";
          let shiftLeft = 0;
          while (diagLeft.length < 4) {
            diagLeft += rows[rowInd + diagLeft.length][i - shiftLeft];
            shiftLeft++;
          }
          if (diagLeft === "XMAS" || diagLeft === "SAMX") {
            sum++;
          }
        }
      }
    }
  }
  return sum;
};

