const turnDude: Record<string, string> = {
  "<": "^",
  "^": ">",
  ">": "v",
  v: "<",
};

// part 1
const getDistinctStepCount = (input: string) => {
  const lines: string[] = input.split("\n");

  let y = lines.findIndex((l) => /(<|>|\^|v)/.test(l));
  // console.log(y, lines);
  let x = lines[y].split("").findIndex((c) => /(<|>|\^|v)/.test(c));

  const linesForLoop: string[][] = lines.map((l) => l.split(""));

  let loopCount = input.length;
  while (loopCount) {
    const dude = linesForLoop[y][x];

    let nextSpace;
    if (dude === ">") {
      nextSpace = linesForLoop[y][x + 1];
    } else if (dude === "<") {
      nextSpace = linesForLoop[y][x - 1];
    } else if (dude === "^") {
      nextSpace = linesForLoop[y - 1]?.[x];
    } else {
      nextSpace = linesForLoop[y + 1]?.[x];
    }

    if (nextSpace === undefined) {
      break;
    }

    if (nextSpace === "#") {
      linesForLoop[y][x] = turnDude[dude];
    } else {
      linesForLoop[y][x] = "X";
      if (dude === ">") {
        x += 1;
      } else if (dude === "<") {
        x -= 1;
      } else if (dude === "^") {
        y -= 1;
      } else {
        y += 1;
      }
      linesForLoop[y][x] = dude;
    }
    loopCount--;
  }

  return loopCount === 0;
  // console.log(
  //   linesForLoop.reduce((acc, l) => {
  //     const numX = l.filter((c) => c === "X").length;
  //     return acc + numX;
  //   }, 0) + 1
  // );
};

