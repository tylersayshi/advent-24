const getNumberOfNewObstacleSpaces = (input: string) => {
  const lines: string[] = input.split("\n");
  let sum = 0;
  for (let i = 0; i < lines.length; i++) {
    const splitLine = lines[i].split("");
    for (let j = 0; j < splitLine.length; j++) {
      const c = splitLine[j];
      // console.log({ c });
      if (/(<|>|\^|v)/.test(c) || c === "#") continue;

      const lineWithObstacle = [
        ...splitLine.slice(0, j),
        "#",
        ...splitLine.slice(j + 1),
      ];
      // console.log({ lineWithObstacle });
      const inputToTry = [
        ...lines.slice(0, i),
        lineWithObstacle.join(""),
        ...lines.slice(i + 1),
      ];

      if (getDistinctStepCount(inputToTry.join("\n"))) {
        sum++;
      }
    }
  }
  console.log(sum);
};

